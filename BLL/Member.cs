		public List<ServiceWeb.Model.Member> DataTableToList(DataTable dt)
		{
			List<ServiceWeb.Model.Member> modelList = new List<ServiceWeb.Model.Member>();
			int rowsCount = dt.Rows.Count;
			if (rowsCount > 0)
			{
				ServiceWeb.Model.Member model;
				for (int n = 0; n < rowsCount; n++)
				{
					model = dal.DataRowToModel(dt.Rows[n]);
					if (model != null)
					{
						modelList.Add(model);
					}
				}
			}
			return modelList;
		}

